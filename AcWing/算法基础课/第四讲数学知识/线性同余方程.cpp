int a, b, m;
int x, y;

int exgcd(int a, int b, int &x, int &y) {
    if (b == 0) {
        x = 1;
        y = 0;
        return a;
    }
    int res = exgcd(b, a % b, y, x);
    y -= a / b * x;
    return res;
}

void solve() {
    cin >> a >> b >> m;

    int res = exgcd(a, m, x, y);
    if (b % res)
        cout << "impossible" << endl;
    else
        cout << x * (b / res) % m << endl;
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while (t--) {
        solve();
    }
    return 0;
}