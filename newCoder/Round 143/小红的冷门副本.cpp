void solve() {
    int n, m, x;
    cin >> n >> m >> x;
    map<int, int> cnt;
    for (int i = 1; i <= n; i++) {
        int idx;
        cin >> idx;
        cnt[idx]++;
    }
    int upper = 0;
    for (auto [idx, c] : cnt) {
        if (c > x)
            upper++;
    }
    cout << m - upper << endl;
}

