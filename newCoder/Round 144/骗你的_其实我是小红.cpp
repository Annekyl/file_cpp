void solve() {
    int l, r, k;
    cin >> l >> r >> k;

    int num = r - l + 1;
    int q = num / k;
    int rem = num % k;
    int cnt1 = rem, cnt2 = k - rem;
    int ans = cnt1 * q * (q + 1) / 2 + cnt2 * q * (q - 1) / 2;
    cout << ans << endl;
}

